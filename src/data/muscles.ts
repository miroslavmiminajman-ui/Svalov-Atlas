export function getMusclesByGroup(groupKey: string): Muscle[] {
  return musclesData.filter(m => m.groupKey === groupKey);
}

export function searchMuscles(query: string): Muscle[] {
  const q = query.toLowerCase().trim();
  if (!q) return musclesData;
  return musclesData.filter(m => 
    m.nameCs.toLowerCase().includes(q) || 
    m.nameLa.toLowerCase().includes(q) || 
    m.origin.toLowerCase().includes(q) || 
    m.insertion.toLowerCase().includes(q) || 
    m.actions.some(a => a.toLowerCase().includes(q)) ||
    m.group.toLowerCase().includes(q)
  );
}
